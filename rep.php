<h2>Description</h2>
<p>The GPSA is part of Cornell&#39;s system of governance, charged with representing the interests of graduate and professional students. The GPSA is derived from a larger grouping of members called the &quot;Council of Representatives&quot; (COR). The COR is composed of representatives from each of the graduate fields and the 3 professional schools. COR meetings are usually held monthly during the academic year and matters of interest to the constituents are brought forth and communicated. It is important that each field/school be represented as its members serve as a conduit of information both from and to the constituency and the University decision makers. Voting GPSA members are elected by and from the COR members during the Spring organizational meeting. These GPSA members attend all COR meetings and also attend a monthly business meeting to act on the issues brought to their attention.&quot; (<a href="http://www.assembly.cornell.edu/gpsa/">http://www.assembly.cornell.edu/gpsa/</a>) <br><br>COR representatives are elected in the Fall for a 1 academic year term. In past years this has been conducted very informally via e-mail to students@cs; contact a current officeholder for more details.</p>

