<h1>Hockey Czar</h1>

<h2>Description</h2>
<p>The Hockey Czars organize ice times for the department's frequent ice hockey scrimmages. This includes buying ice time, maintaining the list of interested members of the department, announcing scrimmages and forming teams, and collecting money from those who play. The Hockey Czar also helps organize players for the department intramural team(s).
<br/>
<br/>
The Hockey Czars are also responsible for the maintenance and distribution of the hockey equipment which is purchased by the "hockey fund" and lent to people who would like to play ice hockey with the department and do not have equipment.</p>

