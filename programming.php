<h2>Description</h2>
<p>In conjunction with the ACSU Programming Chair, the Programming Contest Czar organizes an undergraduate programming contest. They are responsible for writing/obtaining problems and solutions, writing up the problems, writing grading criteria for problems, obtaining judges to help grade problems, recording scores, proclaiming the winner, and clarifying rules and problems when asked. The ACSU Programming Chair reserves the facilities, advertises, and provides food and prizes. <br><br>Additionally, the Programming Contest Czar coaches the ACM Contest teams and submits a budget proposal for training and travel costs for the regional and international contests. The budget for team travel must be submitted by the end of September or early October at the latest. This information needs to be forwarded to the Associate Director of External Relations who works to find corporate sponsors.</p>

